# Дано рядок. Знайдіть у цьому рядку друге входження літери f і виведіть індекс цього
# входження. Якщо літера f у даному рядку зустрічається лише один раз, виведіть число -
# 1, а якщо не зустрічається жодного разу, виведіть число -2

s = input()
cnt = s.count("f")

if cnt > 1:
    print(s.find("f", s.find("f")+1))
elif cnt == 1:
    print(-1)
else:
    print(-2)
