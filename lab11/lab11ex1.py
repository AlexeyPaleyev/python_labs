# У єдиному рядку записаний текст. Для кожного слова з тексту підрахуйте, скільки разів
# воно зустрічалося в цьому тексті раніше.
# Словом вважається послідовність непробільних символів, що йдуть підряд, слова
# розділені одним або більшим числом пробілів або символами кінця рядка.

ar = input().split()
A = dict(zip(list(range(len(ar))), ar))
for key, val in A.items():
    res = 0
    for i in range(int(key)):
        if A[i] == val:
            res += 1
    print(res)
