# Вам дано словник, що складається із пари слів. Кожне слово є синонімом до парного слова.
# Усі слова у словнику різні. Для слова зі словника, записаного в останньому рядку, визначте
# синонім.

n = int(input())
A = {}
B = {}
for i in range(n):
    lst = input().split()
    A[lst[1]] = lst[0]
    B[lst[0]] = lst[1]
word = input()
print(A.get(word, B.get(word)))
