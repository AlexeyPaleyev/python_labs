# Дано чотири дійсні числа: x1, y1, x2, y2. Напишіть функцію distance(x1, y1, x2, y2), яка
# обчислює відстань між точкою (x1, y1) та (x2, y2). Введіть чотири дійсні числа та
# виведіть результат роботи цієї функції.
# Підказка: використайте теорему Піфагора

x1 = float(input())
y1 = float(input())
x2 = float(input())
y2 = float(input())


def distance(r1, c1, r2, c2):
    res = ((r2-r1)**2 + (c2-c1)**2)**0.5
    return(res)


print(distance(x1, y1, x2, y2))
