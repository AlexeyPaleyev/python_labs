# Напишіть функцію fib(n), яка за цим цілим невід'ємним n повертає n-e число Фібоначчі. У
# цій задачі не можна використовувати цикли — використовуйте рекурсію.

def fib(n):
    if n == 1 or n == 2:
        ret = 1
    else:
        ret = fib(n-2) + fib(n-1)
    return ret


nn = int(input())
print(fib(nn))
