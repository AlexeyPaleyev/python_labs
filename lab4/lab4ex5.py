# Дано кілька чисел. Обчисліть їхню суму. Спочатку вводиться кількість чисел N, потім вводиться
# рівно N цілих чисел. Яку найменшу кількість змінних необхідно для розв’язання цієї задачі?

res = 0
for i in range(int(input("кількість чисел "))):
    res = res + int(input("число " + str(i)))
print(res)
