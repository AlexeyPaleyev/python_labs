# У математиці функція sign(x) (знак числа) визначена так:
# sign(x) = 1, если x > 0,
# sign(x) = -1, если x < 0,
# sign(x) = 0, если x = 0.
# Для даного числа x вивідіть значення sign(x). Цю задачу бажано розв’язати з
# використанням каскадних інструкцій if... elif... else.

x = int(input("Число "))

if x < 0:
    res = -1
elif x > 0:
    res = 1
else:
    res = 0
print(res)

