# Дано натуральне число. Потрібно визначити, чи є рік з даними номером високосним.
# Якщо рік є високосний, то виведіть YES, інакше виведіть NO. Нагадаємо, що відповідно до
# григоріанського календаря, рік є високосним, якщо його номер кратний 4, але не кратний
# 100, а також якщо він кратний 400.

year = int(input("Номер року "))
res = 'NO'
if (year % 4 == 0 and year % 100 != 0 and year % 400 != 0):
    res = "YES"
print(res)

