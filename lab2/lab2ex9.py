# Шаховий слон ходить по діагоналі. Дано дві різні клітинки шахової дошки, визначте, чи
# зможе слон потрапити з першої клітинки на другу одним ходом.

c1v = int(input("Вертикаль клітини1 "))
c1g = int(input("Горизонталь клітини1 "))
c2v = int(input("Вертикаль клітини2 "))
c2g = int(input("Горизонталь клітини2 "))

if abs(c1v-c2v) == abs(c1g-c2g):
    print("YES")
else:
    print("NO")
