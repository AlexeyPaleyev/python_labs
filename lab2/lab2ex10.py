# Шаховий ферзь ходить по діагоналі, горизонталі або вертикалі. Дано дві різні клітини
# шахової дошки, визначте, чи може ферзь потрапити з першої клітини на другу одним
# ходом.
# Підказка: дві умови: для офіцера і для тури

c1v = int(input("Вертикаль клітини1 "))
c1g = int(input("Горизонталь клітини1 "))
c2v = int(input("Вертикаль клітини2 "))
c2g = int(input("Горизонталь клітини2 "))

if (abs(c1v-c2v) == abs(c1g-c2g)) or (c1v-c2v == 0 or c1g-c2g == 0):
    print("YES")
else:
    print("NO")
