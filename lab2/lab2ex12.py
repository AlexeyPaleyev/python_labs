# Шоколадка має вигляд прямокутника, розділеного на n × m частинок. Шоколадку можна
# один раз розламати по прямій на дві частини. Визначте, чи можна таким чином
# відламати від шоколадки частину, що складається рівно з k частинок. Програма отримує
# на вхід три числа: n, m, k і повинна вивести YES або NO

n = int(input("Кількість чвстинок1 "))
m = int(input("Кількість чвстинок2 "))
k = int(input("Кількість чвстинок3 "))


if k % n == 0 or k % m == 0:
    print("YES")
else:
    print("NO")
