# Першого дня спортсмен пробіг х кілометрів, а потім він щодня збільшував пробіг на 10%
# від попереднього значення. За даним числом y визначте номер дня, на який пробіг
# спортсмена становитиме не менше у кілометрів.
# Програма отримує на вхід дійсні числа x та y і має вивести одне натуральне число.

x = int(input())
y = int(input())
i=1
while x < y:
    x = x * 1.1
    i+=1
print(i)