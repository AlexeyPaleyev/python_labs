# Послідовність складається з натуральних чисел і завершується числом 0. Визначте,
# скільки елементів цієї послідовності дорівнюють її найбільшому елементу

n = int(input())
m = n
k = 0
while n != 0:
    if n > m:
        k = 1
        m = n
    elif n == m:
        k += 1
    n = int(input())
print(k)
