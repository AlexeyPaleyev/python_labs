# Послідовність складається з натуральних чисел і завершується числом 0. Визначте
# індекс найбільшого елемента послідовності. Якщо найбільших елементів є кілька,
# виведіть індекс першого з них. Нумерація елементів починається з нуля.

m = 0
i = -1
res = 0
n = int(input())
while n != 0:
    i += 1
    if n > m:
        m = n
        res = i
    n = int(input())
print(res)
