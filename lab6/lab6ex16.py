# Дано натуральне число A. Визначте, яким по порядку числом Фібоначчі воно є, тобто
# виведіть таке число n, що φn = A. Якщо А не є числом Фібоначчі, виведіть число -1.

a = int(input())
res = -1
i = 1
fib = 1
fib_p = 0
while fib < a:
    fib, fib_p = fib + fib_p, fib
    i += 1
if fib == a:
    res = i

print(res)
