# Послідовність Фібоначчі визначається так: кожне наступне число дорівнює сумі двох
# попередніх. Тобто: φ0 = 0, φ1 = 1, φn = φn-1 + φn-2.
# По даному числу n визначте n число Фібоначчі φn.
# Це завдання можна вирішувати і циклом for

n = int(input())
i = 0
fib = 0
while i <= n:
    if i == 0:
        fib = 0
        fib_p = 0
    elif i == 1:
        fib = 1
        fib_p = 0
    else:
        fib_n = fib_p + fib
        fib_p = fib
        fib = fib_n
    i += 1
print(fib)
