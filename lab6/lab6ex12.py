# Послідовність складається з різних натуральних чисел і завершується числом 0.
# Визначте значення другого за величиною елемента цієї послідовності. Гарантується, що
# у послідовності є хоча б два елементи.

n = int(input())
m1, m2 = 1, 1
while n != 0:
    if n > m1:
        m2 = m1
        m1 = n
    elif m2 < n and m2 < m1:
        m2 = n
    n = int(input())
print(m2)
