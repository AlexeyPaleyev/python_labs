# Програма отримує послідовність цілих невід'ємних чисел, кожне число записано в
# окремому рядку. Послідовність завершується числом 0, під час зчитування якого
# програма має закінчити свою роботу і вивести кількість членів послідовності (крім
# завершального числа 0). Числа, що йдуть за числом 0, не потрібно зчитувати

i = 0
while True:
    if int(input()) == 0:
        break
    i += 1
print(i)
