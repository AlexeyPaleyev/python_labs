# Дано список чисел. Якщо в ньому є два сусідні елементи одного знака, виведіть ці цифри.
# Якщо сусідніх елементів одного знака немає, не виводьте нічого. Якщо таких пар сусідів
# кілька - виведіть першу пару

a = [int(s) for s in input().split()]
for i in range(1, len(a)):
    if a[i-1] * a[i] > 0:
        print(a[i-1], a[i])
        break
