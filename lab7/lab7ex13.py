# Дано список чисел. Порахуйте, скільки пар пар елементів, рівних один одному.
# Вважається, що будь-які два елементи рівні один одному утворюють одну пару, яку
# необхідно порахувати

a = [int(s) for s in input().split()]
res = 0
for i in range(len(a)):
    for j in range(i + 1, len(a)):
        if a[i] == a[j]:
            res += 1
print(res)
