# Дано список чисел. Виведіть значення найбільшого елемента, а потім індекс цього
# елемента у списку. Якщо найбільших елементів є кілька, виведіть індекс першого з них.

a = [int(s) for s in input().split()]
maks = a[0]
ind = 0
for i in range(1, len(a)):
    if a[i] > maks:
        maks = a[i]
        ind = i
print(maks, ind)