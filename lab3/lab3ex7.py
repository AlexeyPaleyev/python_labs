# Пиріжок в їдальні коштує a гривень і b копійок. Визначте, скільки гривень і копійок
# потрібно заплатити за n пиріжків. Програма отримує на вхід три числа: a, b, n, і повинна
# вивести два числа: вартість покупки в гривнях і копійках

a = int(input("Пиріжок в їдальні коштує гривень "))
b = int(input("Пиріжок в їдальні коштує копійок "))
n = int(input("кількість пиріжків "))
print(a*n+b*n//100, b*n%100)