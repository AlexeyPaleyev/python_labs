# З початку доби годинникова стрілка повернулася на кут в α градусів. Визначте скільки
# повних годин, хвилин і секунд пройшло з початку доби, тобто вирішите задачу, зворотну
# задачі «Годинник - 1». Запишіть відповідь у три змінні і виведіть їх на екран

a = float(input("градусів "))
h = a // 30
m = (a % 30 * (360 / 30 * 60 / 360))
s = (m % 1 * 60)
print(int(h), int(m), int(s))
