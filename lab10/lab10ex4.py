# У вхідному рядку записана послідовність чисел через пропуск. Для кожного числа виведіть
# слово YES (в окремому рядку), якщо це число раніше зустрічалося в послідовності або NO,
# якщо не траплялося.

a = [int(s) for s in input().split()]
A = set()
for i in range(len(a)):
    if a[i] in A:
        print("YES")
    else:
        print("NO")
    A.add(a[i])
